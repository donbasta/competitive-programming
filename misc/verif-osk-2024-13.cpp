ll sum_odd_factor(ll x) {
    ll ret = 0;
    for (ll i = 1; i * i <= x; i++) {
        if (x % i != 0) continue;
        if (i & 1) ret += i;
        if (i * i != x) {
            if ((x / i) & 1) ret += (x / i);
        }
    }
    return ret;
}

