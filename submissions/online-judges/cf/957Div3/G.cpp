const int INF = 2e9;
const ll MOD = 1e9 + 7;
const int M = 5e3;

ll C[M + 5][M + 5];
void precomp() {
    C[0][0] = 1;
    for (int i = 1; i <= M; i++) {
        C[i][0] = C[i][i] = 1;
        for (int j = 1; j < i; j++) {
            C[i][j] = (C[i - 1][j] + C[i - 1][j - 1]) % MOD;
        }
    }
}

void el_psy_congroo() {
    int n;
    cin >> n;

    ll ans = 0;
    for (int t = 1; t <= n; t++) {
        for (int r = 0; r <= t - 1; r++) {
            ll tmp = C[t - 1][r];
            ll cek = t - 2 * r - 1;
            if (n - t >= cek && cek >= 0) {
                tmp = (tmp * C[n - t][cek]) % MOD;
                tmp = (tmp * t) % MOD;
                ans = (ans + tmp) % MOD;
            }
        }
    }
    for (int t = n + 1; t <= 2 * n + 1; t++) {
        if (t % 2 == 0) continue;
        ll tmp = C[n][t / 2];
        tmp = (tmp * t) % MOD;
        ans = (ans + tmp) % MOD;
    }

    if (ans < 0) ans += MOD;
    cout << ans << '\n';
}

