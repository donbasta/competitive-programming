void el_psy_congroo() {
    int n, k;
    cin >> n >> k;
    vector<ll> A(n), B(n), id(n);
    for (int i = 0; i < n; i++) {
        cin >> A[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> B[i];
    }
    iota(id.begin(), id.end(), 0);
    sort(id.begin(), id.end(), [&](int i, int j) -> bool {
        return A[i] < A[j];
    });
    multiset<ll> mse;  // store largest k - 1
    ll totB = 0;
    for (int i = 0; i < k - 1; i++) {
        mse.insert(B[id[i]]);
        totB += B[id[i]];
    }
    ll ans = 2e18;
    for (int i = k - 1; i < n; i++) {
        ans = min(ans, A[id[i]] * (totB + B[id[i]]));
        if (!mse.empty() && B[id[i]] < *mse.rbegin()) {
            ll largest = *mse.rbegin();
            totB += B[id[i]] - largest;
            mse.erase(mse.find(largest));
            mse.insert(B[id[i]]);
        }
    }
    cout << ans << '\n';
}

