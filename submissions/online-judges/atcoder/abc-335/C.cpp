void el_psy_congroo() {
    int n, q;
    cin >> n >> q;
    vector<pair<int, int>> pos;
    for (int i = n; i >= 1; i--) {
        pos.emplace_back(i, 0);
    }
    int hx = 1, hy = 0;
    while (q--) {
        int t;
        cin >> t;
        if (t == 1) {
            char dir;
            cin >> dir;
            if (dir == 'R') {
                hx++;
            } else if (dir == 'L') {
                hx--;
            } else if (dir == 'U') {
                hy++;
            } else if (dir == 'D') {
                hy--;
            }
            pos.emplace_back(hx, hy);
        } else if (t == 2) {
            int p;
            cin >> p;
            --p;
            int sz = pos.size();
            cout << pos[sz - 1 - p].first << ' ' << pos[sz - 1 - p].second << '\n';
        }
    }
}

